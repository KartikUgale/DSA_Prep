public class DataTypes {
    public static void Main(String args[]) {

        // Data Types in Java --> Premetive & Non-Premetive
        // there are all 8 Premetive data types in java

        byte n = 8; // 8 bit Integer -128 to 127
        System.out.println(n);

        short s = 7867; // 16 bit Integer -32,768 to 32,767
        System.out.println(s);

        char a = 'b'; // 16 bit Unicode Character e.g- 'a', '9', '@'
        System.out.println(a);

        int i = 90; // 32 bit Integer -2^31 to 2^31-1
        System.out.println(i);

        float f = 89.7f; // 32 bit floating Point e.g- 1.2f, -2.3f
        System.out.println(f);

        long l = 6574563; // 64 bit Integer -2^63 to 2^63-1
        System.out.println(l);

        double d = 8976; // 64 bit floating Point e.g- 1.289889, -2.3
        System.out.println(d);

        boolean t = false; // 1 byte (Logical : true or False)
        System.out.println(t);

    }
}